package com.example.demo.model.DTO;


public class ChartDTO {
    private String year;
    private double total;

    public ChartDTO() {
    }

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public Double getTotal() {
        return total;
    }

    public void setTotal(Double total) {
        this.total = total;
    }
}
